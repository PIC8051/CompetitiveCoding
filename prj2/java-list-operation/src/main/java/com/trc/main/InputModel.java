package com.trc.main;

public class InputModel {
	int n;
	int[] game;
	int nQueries;
	int[] arrInsert;
	int delPos;

	public int getN() {
		return n;
	}

	public void setN(int n) {
		this.n = n;
	}

	public int[] getGame() {
		return game;
	}

	public void setGame(int[] game) {
		this.game = game;
	}

	public int getnQueries() {
		return nQueries;
	}

	public void setnQueries(int nQueries) {
		this.nQueries = nQueries;
	}

	public int[] getArrInsert() {
		return arrInsert;
	}

	public void setArrInsert(int[] arrInsert) {
		this.arrInsert = arrInsert;
	}

	public int getDelPos() {
		return delPos;
	}

	public void setDelPos(int delPos) {
		this.delPos = delPos;
	}

}
